package ATBOct2024;

public class lab_005 {
    public static void main(String[] args) {

        // Comparison operators
        int a = 10;
        int b = 3;
        int c = 5;
        System.out.println("a > b: " + (a > b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a <= b: " + (a <= b));
        System.out.println("a == c: " + (a == c));
        System.out.println("a != c: " + (a != c));
        System.out.println("Boolean is always either true or false");
    }
}

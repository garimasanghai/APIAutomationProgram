package ATBOct2024;

public class lab_036evenOddUsingLoop {
    public static void main(String[] args) {

        for (int i = 0; i <= 50; i++) {
            if (i % 2 == 0) {
                System.out.println("Even");
            }else{
                System.out.println("Odd");
            }
        }
    }
}
